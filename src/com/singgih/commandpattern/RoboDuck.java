public class RoboDuck {

    public void swimming(){
        System.out.println("do swimming");
    }

    public void floating(){
        System.out.println("just floating");
    }

    public void quack(){
        System.out.println("sound quack quack");
    }

    public void silent(){
        System.out.println("keep silent");
    }

    public void fireMissile(){
        System.out.println("fire a missile underwater");
    }

    public void stopFireMissile(){
        System.out.println("stop firing missile");
    }

}
